package lecture.p0302assignment;

public class C01Assignment {
	public static void main(String[] args) {
		//할당(대입)연산자
		// = , += , -= , *= , /=, %=
		
		int x = 10;
		
		x += 30;
		System.out.println(x);
		
		x -= 20;
		System.out.println(x);
		
		x *= 3; 
		System.out.println(x);
		
		x /= 3;
		System.out.println(x);
		
		x %= 3;
		System.out.println(x);
	}
}
