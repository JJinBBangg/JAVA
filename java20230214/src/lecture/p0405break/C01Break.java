package lecture.p0405break;

public class C01Break {
	public static void main(String[] args) {
		// break
		// 반복문(for, while, do while) 종료

		while (true) {
			System.out.println("코드 반복1");
			System.out.println("코드 반복2");
			break;
		}
		System.out.println("코드 반복1");
		System.out.println("코드 반복2");
		System.out.println("코드 반복1");
		System.out.println("코드 반복2");
	}
}
