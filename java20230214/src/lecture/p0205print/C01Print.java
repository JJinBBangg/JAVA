package lecture.p0205print;

public class C01Print {
	public static void main(String[] args) {
		// println
		// 파라미터 출력 후 엔터 출력
		
		System.out.println("hello");
		System.out.println("world");
		System.out.println(true);
		
		// 파라미터 없이 사용 시 엔터만 출력
		System.out.println();
		System.out.println();
		
		System.out.println("새로운 줄 출력됨");
		
		
	}
}
